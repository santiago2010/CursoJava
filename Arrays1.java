//Um array sempre sera um Objeto
//A palavra NEW é somente usada para objetos
public class Arrays1 {
    public static void main(String[] args){
        int[] idade = new int[3];
        idade[0] = 20;
        idade[1] = 30;
        idade[2] = 40;
        System.out.println("A idade 2 é: "+ idade[1]);
        System.out.println("A idade 1 é: "+ idade[0]);
        System.out.println("A idade 3 é: "+ idade[2]);

    }
}
