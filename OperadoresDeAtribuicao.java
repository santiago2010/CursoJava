public class OperadoresDeAtribuicao {
    public static void main(String[] args) {
        // Operadores de atribuicao: igual =, menos igual -=, soma igual +=, multiplicao igual *=, divi igual /=, resto %=
        int salario = 1000;
        salario += 1000;//soma mil no salario com menos linhas
        int numero = 9;
        numero %= 2;
        System.out.println(numero);
    }
 }
